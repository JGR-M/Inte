const mongoose = require('mongoose');
const Schema = mongoose.Schema;

// create schema 
const UserSchema = new Schema({
    user: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

mongoose.model('user', UserSchema);